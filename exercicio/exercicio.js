"use strict";
function multiplicacao(a, b) {
    return a * b;
}
function saudacao(nome) {
    return `Olá ${nome}`;
}
const resultadoDaMultiplicacao = multiplicacao(22, 4);
const mensagemSaudacao = saudacao("Ramon Galdino");
console.log("Resultado da multiplicação:", resultadoDaMultiplicacao);
console.log("Mesagem de saudação:", mensagemSaudacao);
